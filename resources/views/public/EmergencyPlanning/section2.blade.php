					<div class="im-box-section">
						<p>Our Practice has appointed an ‘<strong>Emergency Management Coordinator</strong>’. Key functions include:</p>
						<ul class="im-list im-list-lblue">
							<li>preparing and updating (bi-annually) an Emergency Action Plan (<strong>EAP</strong>) for the Practice</li>
							<li>educating and training Practice staff to ensure they have up-to-date knowledge and skills relating to emergency planning and management, including the Practice EAP</li>
							<li>testing or exercising the EAP (or components of the EAP) annually</li>
							<li>ensuring Practice staff can access the EAP at any time</li>
							<li>making decisions as to whether/when the EAP needs to be activated</li>
							<li>building and maintaining relationships with other nearby practices </li>
							<li>connecting with local council and local emergency services</li>
						</ul>
